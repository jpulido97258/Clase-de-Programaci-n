import math 
print("Calculadora de hipotenusa")
print("Inserte el cateto opuesto")
opuesto=int(input())
print ("inserte el cateto adyasente")
adyasente=int(input())
hipotenusa= math.sqrt((opuesto**2)+(adyasente**2))
print("El valor de la hipotenusa es: " ,hipotenusa ) 